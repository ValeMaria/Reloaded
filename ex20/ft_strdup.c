#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int		a;
	char	*d;

	a = ft_strlen(src);
	if (!(d = malloc((a + 1) * sizeof(char))))
		return (NULL);
	ft_strcpy(d, src);
	return (d);
}
